    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int Celsius;
        int Fahrenheit;
        double Resultado=0;
        
        if (TempCelsius.getText().length()>0 && TempFarenheit.getText().length()==0) 
        {
            Celsius=Integer.parseInt(TempCelsius.getText());
            Resultado=ConvertirCelciusFahrenheit(Celsius);
            TempFarenheit.setText(Double.toString(Resultado));
        }
        else if (TempFarenheit.getText().length()>0 && TempCelsius.getText().length()==0) {
            Fahrenheit=Integer.parseInt(TempFarenheit.getText());
            Resultado=ConvertirFahrenheitCelsius(Fahrenheit);
            TempCelsius.setText(Double.toString(Resultado));
        }
        else {
            JOptionPane.showMessageDialog(null, "Digite un solo valor");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

