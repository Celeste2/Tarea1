private double ConvertirCelciusFahrenheit(int temperatura){
    double resultado=0;
    resultado= (temperatura * 9/5) + 32;
            return resultado;
}

private double ConvertirFahrenheitCelsius(int temperatura) 
{
       double resultado=0;
    resultado= (temperatura - 32) * 5/9 ;
    return resultado;
}

